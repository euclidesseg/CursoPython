# EN PYTHON EL CONSTRUCTOR LIST() SE USA CONTINUAMENTE PARA CONVERTIR OBJETOD ITERABLES A LSITA
# SU SINTAXIS ES LA SIGUIENTE:

'''
list(objeto_iterable)
'''
# Ejemplos

numeros = list(range(0, 100, 10))
print(numeros)


print('Lista de numeros de 10 en 10 \n')
caracteres = list('Euclides')

print('Eulides \n ')
print('Lista de caracteres \n')

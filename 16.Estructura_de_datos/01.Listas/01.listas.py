'''
En programacion las estructuras de datos son aquellas que nos permiten organizar la informacion de manera eficiente 
y asi diseñar alternativas de solucion para un determinado problema
una de las estructuras de datos para python mas sencillas son las listas

En python las listas pueden ser homogeneas, es desir, que todos los elementos
que conforman a la lista son del mismo tipo de datos

tambien pueden ser heterogeneas es decir, que los elementos que las conforman son de diferentes 
tipos de datos 

tambien tienen la carateristica de ser mutanble es decir, que sus elementos se pueden 
modificar despues de ser creadas 
'''
# Sintaxis
# ejemplo lista homogenea
lista_homogenea = ['Cristiano', 'messi', 'ronaldiño', 'beckan', 'marcelo']
print('\n lista homogenea')
print(lista_homogenea)
print(len(lista_homogenea))
 
# Ejemplo lista heterogenea
print('\n lista heterogenea')
lista_heterogenea = [10, 'diestefano', 3.14, True]
print(lista_heterogenea)
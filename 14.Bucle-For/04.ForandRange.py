# EN ESTE CAPITULO APRENDEREMOS EL COMPORTAMEIENTO DEL SICLO FOR EN CONJUNTO CON LA CLASE RANGE
# SABEMOS QUE PARA USAR UN FOR DEBEMOS UN OBJETO ITERABLE, BUENO, PARA USAR LA CLASE RANGE JUNTO CON EL FOR 
# REEMPLAZAMOS EL OBJETO ITERABLE POR LA CLASE RANGE

for variable in range(5): # imprimira de 0 a 4
    print(variable)
    
for variable in range(5, 15): # imprimira de 5 a 14
    print(variable)
     
    
for variable in range(5, 16, 2): # imprimira desde 5 a 15 pero iterando de 2 en 2
    print(variable)